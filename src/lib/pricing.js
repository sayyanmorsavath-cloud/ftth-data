// ─── ດຶງລາຄາຈາກ speed + ຈຳນວນເດືອນທີ່ຈ່າຍ ──────────────────
export function getPrice(speed, paidMonths) {
  if (!speed || !paidMonths) return null;
  return PRICE_TABLE[speed]?.[paidMonths] ?? null;
}

// ─── Format ລາຄາ → "1,980,000 ກີບ" ─────────────────────────
export function formatPrice(price) {
  if (price == null) return "—";
  return price.toLocaleString("en-US") + " ກີບ";
}

